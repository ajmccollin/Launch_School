#Adding
numbers = [1, 2]
print(numbers)

numbers.append(4)
print(numbers)

numbers.insert(2, 3)
print(numbers)

numbers.extend([5, 6])
print(numbers)

#Removing
numbers.remove(4)
print(numbers)

numbers.pop(3)
print(numbers)

numbers.pop()
print(numbers)

numbers.clear()
print(numbers)   #Empty collection